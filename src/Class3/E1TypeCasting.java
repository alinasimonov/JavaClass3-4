package Class3;

public class E1TypeCasting {
    public static void main(String[] args) {

        //byte myAge=age;
        byte number=10;
        int number2=number;
        System.out.println(number2);


        //byte ->short -> int -> long -> float -> double
        float num1=15.6f;
        int num2;
        num2=(int)num1;

        System.out.println(num2);
    }
}
